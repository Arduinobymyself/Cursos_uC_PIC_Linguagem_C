/*
  CONVERSOR A/D  e PWM
  Controle de Luminosidade Ambiente com LDR e Display 7 Segmentos
  DISP1 e DISP2 (RC6 e RC7)
  Leitura do canal analógico AN1(RA1) LDR

  Ao ligar o uC, os displays mostram a luminosidade ambiente em %
  Ao acionar o botão B0(MÁX) os displays mostram a intensidade luminosa máxima medida
  Ao acionar o botão B1(MÍN) os displays mostram a intensidade luminosa mínima medida
  volta a condição inicial após novo pressionamento do referido botão

*/

// --- inclusão de bibliotecas --- //

// --- definições do sistema --- //
#define B0  Button(&PORTB, 0, 50, 0)
#define B1  Button(&PORTB, 1, 50, 0)

// --- protótipod das funções auxiliares --- //
void display(unsigned char lux);

// --- variáveis globais --- //
unsigned int leituraAD;
unsigned char minima, maxima;
unsigned char luminosidade = 0;
unsigned char digitos[] = {63, 6, 91, 79, 102, 109, 125, 7, 127, 111};
bit flagB0, flagB1;


// --- programa principal --- //
void main() {

  TRISD = 0b00000000;        // configura PORT D  como saída
  PORTD = 0b00000000;        // inicia em nível baixo
  TRISB = 0b00000011;        // RB0 e RB1 como entrada (botões)
  PORTB = 0b00000011;        // RB0 e RB1 são botões com pull-up
  TRISC = 0b00000000;
  PORTC = 0b00000000;

  TRISA0_bit = 1;            // AN0(RA0) configurado como entrada
  ADC_Init();                // inicializa o ADC com configurações padrão (clock interno RC)
  PWM1_Init(500);            // inicializa o módulo PWM em 500Hz
  PWM1_Start();              // inicia o PWM
  

  minima = 99;
  maxima = 0;

  while(1){

    leituraAD = ADC_Read(0);   // lê o canal analógico 1; converte tensão para valor digital
                               // 5V -> 1023

    luminosidade = leituraAD*0.0978; // 1023 -> 100% ; converte valor digital intensidade luminosa
    PWM1_Set_Duty(leituraAD/4);      // seta o duty-cycle de acordo com a intensidade luminosa
                                     // divide por 4 para transformar de 10 bits para 8 bits
                                     
    // testa os limites
    if(luminosidade > 99) luminosidade = 99;
    if(luminosidade < 0) luminosidade = 0;

    // armazena a máxima e mínima intensidade luminosa medida
    if(luminosidade > maxima) maxima = luminosidade;
    if(luminosidade < minima) minima = luminosidade;

    display(luminosidade);  // chama a função que mostra a luminosidade no display

    // botão B0 mostra a máxima luminosidade
    if(Button(&PORTB, 0, 10, 0)){
      flagB0 = 1;
    }
    if(Button(&PORTB, 0, 10, 1) && flagB0){
      flagB0 = 0;
      while(1){
      display(maxima);
      if(Button(&PORTB, 0, 10, 0)){
        flagB0 = 1;
      }
      if(Button(&PORTB, 0, 10, 1) && flagB0){
        flagB0 = 0;
        break;
      }

      }
    }

    // botão B1 mostra a mínima luminosidade
    if(Button(&PORTB, 1, 10, 0)){
      flagB1 = 1;
    }
    if(Button(&PORTB, 1, 10, 1) && flagB1){
      flagB1 = 0;
      while(1){
      display(minima);
      if(Button(&PORTB, 1, 10, 0)){
        flagB1 = 1;
      }
      if(Button(&PORTB, 1, 10, 1) && flagB1){
        flagB1 = 0;
        break;
      }

      }
    }

  }

}

// --- funções auxiliares --- //
// mostra uma temperatura no display
void display(unsigned char lux){

  char dezena, unidade;

  dezena = lux/10;
  RC6_bit = 1;
  PORTD = digitos[dezena];
  delay_ms(2);
  RC6_bit = 0;
  PORTD = 0;

  unidade = lux%10;
  RC7_bit = 1;
  PORTD = digitos[unidade];
  delay_ms(2);
  RC7_bit = 0;
  PORTD = 0;

}